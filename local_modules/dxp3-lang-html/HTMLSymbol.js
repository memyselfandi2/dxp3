const HTMLElement = require('./HTMLElement');

class HTMLSymbol extends HTMLElement {

    constructor(_entityNumber, _entityName, _description) {
        super();
        this.entityNumber = _entityNumber;
        this.entityName = _entityName;
        this.description = _description;
    }

    static getHTMLSymbol(entityReference) {
        let result = HTMLSymbol.ENTITY_UNKNOWN;
        if(entityReference != undefined && entityReference != null) {
            entityReference = entityReference.trim();
            entityReference = entityReference.replace(/[&#;]/g, '');
            result = HTMLSymbol.entities[entityReference];
            if(result === undefined || result === null) {
                result = HTMLSymbol.ENTITY_UNKNOWN;
            }
        }
        return result;
    }
    getText() {
        return this.toString();
    }

    get text() {
        return this.getText();
    }
    /**
     * @override
     */
    toString() {
        return '&#' + this.entityNumber + ';';
    }
}

